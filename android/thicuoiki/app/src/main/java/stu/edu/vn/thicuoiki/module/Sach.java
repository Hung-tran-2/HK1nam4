package stu.edu.vn.thicuoiki.module;

public class Sach {
    private int ma;
    private String ten;

    public Sach() {
    }

    public int getMa() {
        return ma;
    }

    public void setMa(int ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public Sach(int ma, String ten) {
        this.ma = ma;
        this.ten = ten;
    }

}
